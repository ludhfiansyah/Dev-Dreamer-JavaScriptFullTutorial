// This is an example of using a regular If Statment
let age = 18;

if (age >= 18) {
  console.log("You can vote");
} else {
  console.log("Sorry, you can't vote");
}

// This is if we use Ternary Operator
let message = age >= 30 ? "You can vote" : "Sorry, you can't vote";
console.log(message);

// This is other example if we use Ternary Operator
let password = "redruby123";
let message2 = password === "redruby123" ? "Success" : "Fail";
console.log(message2);

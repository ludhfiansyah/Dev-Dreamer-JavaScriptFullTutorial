/*
1. Create an array of five colors, use a for loop to iterate over each color
   with the statement: '[color] is my fav color!' Console log the results.
*/
const colors = ["white", "red", "blue", "yellow", "black"];
for (let i = 0; i < colors.length; i++) {
  let info = `${colors[i]} is my fav color!`;
  console.log(info);
}
